#include "../fdf.h"

int	main(int argc, char *argv[])
{
	t_data	d;

	initialize(&d, argv[1]);
	put_obj_to_origin(d.obj);
	transform(d.obj, &d);
	render(&d);
	mlx_key_hook(d.win, key_handler, &d);
	mlx_hook(d.win, 17, 0, close_window, NULL);
	mlx_loop(d.mlx);
	return (0);
}